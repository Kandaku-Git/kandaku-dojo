// Construit les tuiles d'histoires dans #section-histoires
window.renderHistoryTiles = function () {
  const container = document.querySelector("#section-histoires .history-tiles");
  const contentCard = document.querySelector("#historyContent");

  if (!container || !Array.isArray(window.HISTOIRES)) return;

  const items = window.HISTOIRES.map((histoire) => ({
    label: histoire.label,
    iconSrc: "",
    description: "",
    dataAttrs: { historyId: histoire.id },
  }));

  window.renderCategoryTiles(container, items, {
    baseClass: "technique-card",
    tileClass: "history-tile card-history",
    onClick: (item, button) => {
      const historyId = item.dataAttrs.historyId;
      const histoire = window.HISTOIRES.find((h) => h.id === historyId);
      if (!histoire || !contentCard) return;

      // état actif
      container
        .querySelectorAll(".history-tile")
        .forEach((t) => t.classList.remove("is-active"));
      button.classList.add("is-active");

      // afficher le cadre + contenu
      contentCard.hidden = false;
      contentCard.innerHTML = `
        <h3>${histoire.label}</h3>
        ${histoire.html}
      `;
    },
  });

  // Au chargement : aucune histoire sélectionnée → on masque le cadre
  if (contentCard) {
    contentCard.hidden = true;
    contentCard.innerHTML = "";
  }
};
